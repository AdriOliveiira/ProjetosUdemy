nome = 'Luiz Otávio'
altura = 1.80
peso = 95
imc =  peso / altura ** 2  # IMC = peso / (altura x altura)


# Luiz Otávio tem 1.80 de altura, 
#pesa 90 quilos e seu IMC é 
# 20.061728395061728
linha_1 = f'{nome} tem {altura:.2f} de altura'
linha_2 = f'pesa {peso} quilos e seu imc é'
linha_3 = f'{imc:.2f}'



print(linha_1)
